package pk.codeapp.model;

/**
 *
 * @author Daniel Amador
 */
public class Dupla
{
    int posX;
    int posY;

    public Dupla(int posX, int posY)
    {
        this.posX = posX;
        this.posY = posY;
    }

    public int getPosX() {
        return posX;
    }

    public void setPosX(int posX) {
        this.posX = posX;
    }

    public int getPosY() {
        return posY;
    }

    public void setPosY(int posY) {
        this.posY = posY;
    }
    
    
}
